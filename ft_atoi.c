int	ft_atoi(char *str)
{
	int	signe;
	int	nbr;

	signe = 1;
	nbr = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			signe *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nbr *= 10;
		nbr += (int)(*str - 48);
		str++;
	}
	return (nbr * signe);
}
