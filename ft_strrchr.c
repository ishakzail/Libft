char	*ft_strrchr(const char *s, int c)
{
	char	*str;
	char	*p_char;

	str = (char *) s;
	if (!str)
		return (0);
	while (*str != '\0')
	{
		if (*str == c)
			p_char = str;
		str++;
	}
	return (p_char);
}
