#include <stddef.h>

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = str;
	if (*ptr == 0)
		return (0);
	while (*ptr != '\0' && n > 0)
	{
		*ptr = c;
		ptr++;
		n--;
	}
	return (ptr);
}
