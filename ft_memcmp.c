#include<stddef.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const char	*p1;
	const char	*p2;

	p1 = s1;
	p2 = s2;
	if (n == 0)
		return (0);
	while ((*p1 == *p2) && n--)
	{
		s1++;
		s2++;
	}
	return (*p1 - *p2);
}
